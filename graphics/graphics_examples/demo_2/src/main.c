void main(void) {
	uint8_t key = 0;
	gc_tilemap_t tilemap;
	unsigned x_offset = 0, y_offset = 0;
	
	tilemap.tiles = tilemap_tiles;
	tilemap.map = tilemap_map;
	tilemap.tile_height = 16;
	tilemap.tile_width = 16;
	tilemap.height = 12;
	tilemap.width = 42;
	tilemap.y_loc = 0;
	tilemap.x_loc = 0;

	/* Initialize the graphics */
	gc_InitGraph();
	
	gc_SetPalette(tiles_gfx_pal,sizeof(tiles_gfx_pal));
	gc_SetTransparentColor(tiles_gfx_transpcolor_index);
	
	gc_DrawTilemap(&tilemap, x_offset, y_offset);
	
	gc_DrawBuffer();
	
	while(kb_ScanGroup(kb_group_1) != kb_2nd) {
		key = kb_ScanGroup(kb_group_7);
		if (key) {
			gc_FillScrn(0xFF);
			gc_DrawTilemap(&tilemap, x_offset, y_offset);
			switch(key) {
			    case kb_Down:
				y_offset+=4;
				break;
			    case kb_Right:
				x_offset+=4;
				break;
			    case kb_Up:
				y_offset-=4;
				break;
			    case kb_Left:
				x_offset-=4;
				break;
			    default:
				break;
			}
			gc_SwapDraw();
		}
		
	}
	
	/* Close the graphics and return to the OS */
	gc_CloseGraph();
	pgrm_CleanUp();
}
